package org.example;

public class Calculator {

    public int divideTwoNumber(int a, int b) {
        return a / b;


    }
    public int minusTwoNumber(int a , int b) {
        return a - b;
    }

    public int sumTwoNumber(int a , int b) {

        return a + b;
    }
    public int multiTwoNumber(int a, int b) {
        return a * b;
    }
}
